package basic;

public class Days {
	public static void main(String[]args) {
		String dayOfWeek = "Tuesday";
		
		switch(dayOfWeek) {
		case "Monday":
			System.out.println("Monday");
			break;
		case "Tuesday":
			System.out.println("Tuesday");
			break;
		case "Wednesday":
			System.out.println("Wednesday");
			break;
		}
	}
}
